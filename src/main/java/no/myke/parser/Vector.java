package no.myke.parser;

public class Vector
{
    float x, y, z;
    
    Vector (float x, float y, float z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public float X()
    {
        return x;
    }

    public float Y()
    {
        return y;
    }

    public float Z()
    {
        return z;
    }
}
